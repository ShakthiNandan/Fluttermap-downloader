import 'dart:io';
import 'package:flutter/foundation.dart';
import 'package:flutter/widgets.dart';
import 'package:flutter_map/flutter_map.dart';

/// Custom tile provider that loads tiles from local storage (extracted ZIP)
class OfflineTileProvider extends TileProvider {
  final Directory tilesDirectory;

  OfflineTileProvider({required this.tilesDirectory});

  @override
  ImageProvider getImage(TileCoordinates coordinates, TileLayer options) {
    final tilePath = '${tilesDirectory.path}/${coordinates.z}/${coordinates.x}/${coordinates.y}.png';
    final tileFile = File(tilePath);

    if (tileFile.existsSync()) {
      return FileImage(tileFile);
    }

    // Return a placeholder if tile doesn't exist
    return MemoryImage(_transparentPixel);
  }

  /// 1x1 transparent PNG pixel
  static final Uint8List _transparentPixel = Uint8List.fromList([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
    0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
    0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4, 0x89, 0x00, 0x00, 0x00,
    0x0A, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00,
    0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00, 0x00, 0x00, 0x00, 0x49,
    0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82,
  ]);
}
